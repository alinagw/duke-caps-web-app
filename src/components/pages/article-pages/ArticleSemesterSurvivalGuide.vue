    <h4>Exercise</h4>
    <p>Physical activity is one of the best ways to decrease cortisol levels. A simple 20-minute walk can also help get the blood flowing back into your brain after sitting still for extended periods. Inadequate blood flow to the brain can contribute to memory loss.</p>
    <h4>Sleep</h4>
    <p>Short-term memory is converted to long-term memory as you sleep. The more REM cycles you experience in a sleep event the better the transfer. Your brain actually clears out toxins while you sleep - Lack of sleep raises cortisol levels and can impede your ability to perform routine tasks. As stress increases in your daily life it becomes even more important to get at least 6 hours of sleep per night; 8 is better, but at least 6.</p>
    <h4>Breathe Deep</h4>
    <p>Deep inhales through diaphragmatic breathing helps to slow down the central nervous system and provides necessary oxygen that fuels your body. These slow, intentional inhales calm the nerves and relaxes muscles.
