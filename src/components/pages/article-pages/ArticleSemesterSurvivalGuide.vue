    <h4>Take Breaks</h4>
    <p>Memory is highly influenced by recency and primacy effect, which means that you remember what you study first and last. If you have study periods longer than an hour or two, you are more likely to forget what you studied in the middle. A good rule to follow is that for every 45 minutes of studying you should take a 15-minute break to clear your mind and relax.</p>
    <h4>Eat Antioxidant-rich Foods.</h4>
    <p>Stress produces cortisol, which promotes the release of free radicals that can kill brain cells. Eating foods rich in antioxidants like fruits, vegetables, nuts, beans, and dark chocolate can help combat free radicals.
