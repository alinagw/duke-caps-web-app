    <h1>Survival Guide to the End of the Semester</h1>
    <p>Another semester is coming to an end, which means that many of you see cramming and all-nighters in your future. Although this might seem like the best or only approach, there are others techniques that can yield better results. Don’t wear stress like a badge of courage to demonstrate how hard you are working. It’s time to shift the culture and think about an approach that enhances brain function through better wellness practices.</p>
    <p>
        When we feel stressed out, we experience a “fight or flight” toward a perceived threat. For finals, the threat may be the possibility of failure or negative social comparison if you don’t do as well as your peers. When this occurs, the stress hormone cortisol is released into the blood and it can limit your ability to concentrate, decrease your capacity to remember information, suppress your immune system, and may literally kill brain cells. So, how can you keep your stress/cortisol levels down?</p>
    <h4>Set Achievable Goals</h4>
    <p>Dividing out the tasks you want to accomplish into small, achievable goals gives you the opportunity to experience small successes along the way. Success is associated with a release of endorphins into the blood, which are called “feel good hormones”.</p>
    <h4>Set a Schedule</h4>
    <p>Placing your goals on a schedule will keep you anchored in the present so that you can let go of worrying about what you have to do next. Your schedule should contain study breaks so you can eat, exercise, and sleep. Planning these breaks with friends also can be an efficient way to keep you connected to needed support.</p>
    <h4>Practice Mindful Meditation</h4>
    <p>This strategy, practiced for only 15-20 minutes per day will calm and focus your mind, as well as temper strong feelings of anxiety that can be overwhelming.
